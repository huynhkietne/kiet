package com.cntt2.lop;

public class MonHoc {
    public int id;
    public String tenmon;
    public int sotiet;

    public MonHoc() {
        this.id = id;
        this.tenmon = tenmon;
        this.sotiet = sotiet;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTenmon() {
        return tenmon;
    }

    public void setTenmon(String tenmon) {
        this.tenmon = tenmon;
    }

    public int getSotiet() {
        return sotiet;
    }

    public void setSotiet(int sotiet) {
        this.sotiet = sotiet;
    }
}
